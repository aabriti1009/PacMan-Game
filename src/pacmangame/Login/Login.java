    /**
     * Setup password toggle functionality
     */
    private void setupPasswordToggle() {
        // Set initial state - password hidden
        passwordVisible = false;
        updateEyeIcon();
    }

    /**
     * Toggle password visibility
     */
    private void togglePasswordVisibility() {
        passwordVisible = !passwordVisible;
        
        if (passwordVisible) {
            // Show password as plain text
            passwordfield.setEchoChar((char) 0);
        } else {
            // Hide password with dots
            passwordfield.setEchoChar('â€¢');
        }
        
        updateEyeIcon();
    }

    /**
     * Update eye icon based on password visibility state
     */
    private void updateEyeIcon() {
        if (passwordVisible) {
            // Show "show" icon when password is visible
            eye_toggle.setIcon(new javax.swing.ImageIcon(getClass().getResource("/pacmangame/Login/show (1).png")));
        } else {
            // Show "eye" icon when password is hidden
            eye_toggle.setIcon(new javax.swing.ImageIcon(getClass().getResource("/pacmangame/Login/eye (1).png")));
        }
    }

